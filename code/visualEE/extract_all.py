device = "cuda" if torch.cuda.is_available() else "cpu"
model, preprocess = clip.load("ViT-B-32.pt", device=device, jit=False)

class color:
   PURPLE = '\033[95m'
   CYAN = '\033[96m'
   DARKCYAN = '\033[36m'
   BLUE = '\033[94m'
   GREEN = '\033[92m'
   YELLOW = '\033[93m'
   RED = '\033[91m'
   BOLD = '\033[1m'
   UNDERLINE = '\033[4m'
   END = '\033[0m'

img = preprocess(Image.open("123.jpg")).unsqueeze(0).to(device)
texts = ["people"]
text = clip.tokenize(texts).to(device)

R_image = interpret(model=model, image=img, texts=text, device=device)
batch_size = text.shape[0]
for i in range(batch_size):
  print(color.BOLD + color.PURPLE + color.UNDERLINE + 'text: ' + texts[i] + color.END)
  show_image_relevance(R_image[i], img)
